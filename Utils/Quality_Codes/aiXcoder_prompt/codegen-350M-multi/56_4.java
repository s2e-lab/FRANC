import java.io.*;
import java.lang.*;
import java.util.*;
import java.math.*;


class ID56{
	/**
	* 判断是否只含字母
	*/
	public static boolean isLetter(char c){
		//获取首字母
		if(Character.toLowerCase(c)=='a'&&c!='A')return true;//相等时
		if(Character.toLowerCase(c)=='b'&&c!='B')return true;//相等时
		if(Character.toLowerCase(c)=='c'&&c!='C')return true;//相等时
}
}